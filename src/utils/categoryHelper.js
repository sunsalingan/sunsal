export const normalizeCategory = (rawCategory) => {
    if (!rawCategory) return "기타";
    const cat = rawCategory.replace(/\s/g, ""); // Remove spaces

    // 한식
    if (["한식", "국밥", "찌개", "전골", "한정식", "삼겹살", "갈비", "곱창", "족발", "보쌈", "냉면", "분식", "백반", "죽", "국수"].some(v => cat.includes(v))) return "한식";

    // 일식
    if (["일식", "스시", "초밥", "돈가스", "돈까스", "라멘", "우동", "이자카야", "텐동", "덮밥", "가츠동", "소바", "일식당"].some(v => cat.includes(v))) return "일식";

    // 중식
    if (["중식", "짜장", "짬뽕", "탕수육", "마라", "양꼬치", "중국", "딤섬"].some(v => cat.includes(v))) return "중식";

    // 아시안
    if (["아시안", "태국", "베트남", "인도", "쌀국수", "팟타이", "커리", "카레", "똠양꿍", "반미", "나시고랭", "아시아"].some(v => cat.includes(v))) return "아시안";

    // 양식 (포함: 피자, 치킨, 버거, 파스타 등)
    if (["양식", "파스타", "피자", "스테이크", "버거", "햄버거", "멕시칸", "이탈리안", "패밀리레스토랑", "치킨", "샐러드", "브런치", "타코", "바베큐"].some(v => cat.includes(v))) return "양식";

    // 카페/디저트
    if (["카페", "커피", "디저트", "베이커리", "빵", "케이크", "빙수", "아이스크림", "도넛"].some(v => cat.includes(v))) return "카페";

    // 술집 (기타로 분류하거나 별도?) -> 분류 필터에 없으므로 '기타' 혹은 성격에 따라. 
    // 보통 이자카야는 일식, 호프는 양식/한식 애매함.
    // '주점', '술집', '포차' -> 한식 계열이 많지만... 
    // 앱 필터가 [전체, 한식, 일식, 양식, 중식, 카페] 이므로 '기타'로 빠지면 필터링에 안걸림.
    // '술집' -> '한식'으로? 아니면 '양식'으로?
    // 일단 '기타'로 둠.

    return "기타";
};
